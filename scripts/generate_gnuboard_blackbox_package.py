ROOT = Path(__file__).resolve().parents[1]


def _extract_dataset_from_html(html: str):
    m = re.search(r"const dataset = (\[.*?\]);", html, flags=re.S)
    if not m:
        raise ValueError("dataset block not found in source html")
    payload = m.group(1)
    try:
        return json.loads(payload)
    except json.JSONDecodeError:
        # Standalone html may contain escaped JSON payload inside JS string.
        unpacked = codecs.decode(payload, "unicode_escape")
        return json.loads(unpacked)


def _write_dataset_php(dataset, out_path: Path):
    json_text = json.dumps(dataset, ensure_ascii=False, separators=(",", ":"))
    php = (
        "<?php\n"
        "// Auto-generated by scripts/generate_gnuboard_blackbox_package.py\n"
        "$json = <<<'JSON'\n"
        f"{json_text}\n"
        "JSON;\n"
        "$rows = json_decode($json, true);\n"
        "if (!is_array($rows)) { $rows = array(); }\n"
        "return $rows;\n"
    )
    out_path.parent.mkdir(parents=True, exist_ok=True)
    out_path.write_text(php, encoding="utf-8")


