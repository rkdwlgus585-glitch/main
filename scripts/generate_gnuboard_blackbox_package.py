def _write_readme(dataset_count: int, out_path: Path):
    md = f"""# 그누보드5 블랙박스 배포 패키지

생성 시각: 자동 생성
데이터셋 건수: {dataset_count}

## 포함 파일
- `api_calc.php`: 서버측 산정 API (POST JSON)
- `yangdo_dataset.php`: 서버측 학습 데이터셋 (PHP include)

## 배포 위치 권장
- `api_calc.php` → `seoulmna.co.kr` 웹루트
- `yangdo_dataset.php` → 웹루트 하위 `include/` 또는 외부 비공개 경로

## 보안 권장
- `api_calc.php`는 same-origin만 허용하도록 도메인 체크 추가 가능
- 요청 빈도 제한(예: 분당 60회/IP)을 서버 레벨로 추가
- `yangdo_dataset.php`는 직접 URL 접근 차단(.htaccess / nginx location deny)

## 프론트엔드 연결
- `.env`에 `YANGDO_ESTIMATE_ENDPOINT=https://seoulmna.co.kr/api_calc.php` 설정
- `py -3 scripts/deploy_yangdo_site_release.py --publish` 재실행 시 브라우저에서 원본 dataset 미노출 모드 동작
"""
    out_path.parent.mkdir(parents=True, exist_ok=True)
    out_path.write_text(md, encoding="utf-8")


def main():
    parser = argparse.ArgumentParser(description="Generate gnuboard5 blackbox package (api_calc.php + dataset php)")
    parser.add_argument(
        "--source-html",
        default="output/yangdo_price_calculator_owner_standalone.html",
        help="HTML file that contains const dataset = [...]",
    )
    parser.add_argument("--out-dir", default="output/deploy/gnuboard_blackbox")
    args = parser.parse_args()

    src = (ROOT / args.source_html).resolve()
    if not src.exists():
        raise FileNotFoundError(f"source html not found: {src}")
    html = src.read_text(encoding="utf-8", errors="replace")
    dataset = _extract_dataset_from_html(html)
    out_dir = (ROOT / args.out_dir).resolve()
    dataset_path = out_dir / "yangdo_dataset.php"
    api_path = out_dir / "api_calc.php"
    readme = out_dir / "README.md"

    _write_dataset_php(dataset, dataset_path)
    _write_api_php("yangdo_dataset.php", api_path)
    _write_readme(len(dataset), readme)

    print(f"[ok] generated package in {out_dir}")
    print(f"[dataset_rows] {len(dataset)}")
    print(f"[api] {api_path}")
    print(f"[dataset] {dataset_path}")
    print(f"[readme] {readme}")


if __name__ == "__main__":
    main()
