def _parse_eok_amount(amount_text: str):
    import re

    s = re.sub(r"\s+", "", str(amount_text or ""))
    m = re.match(
        r"^(?P<eok>\d+(?:\.\d+)?)억(?:(?P<thousand>\d+)천)?(?:(?P<man>\d{1,4})만)?(?:원)?$",
        s,
    )
    if not m:
        return None

    eok = float(m.group("eok"))
    if "." not in m.group("eok"):
        if m.group("thousand"):
            eok += int(m.group("thousand")) / 10.0
        if m.group("man"):
            eok += int(m.group("man")) / 10000.0
    return round(eok, 4)


def validate_fact(text: str) -> list:
    """텍스트에서 팩트 오류 검출"""
    errors = []

    import re

    amount_token = r"([0-9]+(?:\.[0-9]+)?\s*억(?:\s*(?:[0-9]+\s*천)?\s*(?:[0-9]{1,4}\s*만)?\s*원?)?)"
    patterns = [
        (rf"(토목건축공사업)[^0-9\n]{{0,40}}?{amount_token}", "토목건축공사업", 8.5),
        (rf"((?<!토목)건축공사업)[^0-9\n]{{0,40}}?{amount_token}", "건축공사업", 3.5),
        (rf"(토목공사업)[^0-9\n]{{0,40}}?{amount_token}", "토목공사업", 5.0),
    ]

    seen = set()
    for pattern, 업종, 정답 in patterns:
        for m in re.finditer(pattern, text):
            amount_text = m.group(2)
            금액 = _parse_eok_amount(amount_text)
            if 금액 is None:
                continue

            # 개인 자본금 라인(예: 17억원)은 법인 자본금 기준 검증에서 제외.
            context = m.group(0)
            head = context.split(amount_text, 1)[0]
            if "개인" in head and "법인" not in head:
                continue

            if abs(금액 - 정답) > 0.1:
                key = (업종, 금액, 정답)
                if key in seen:
                    continue
                seen.add(key)
                errors.append(
                    {
                        "type": "자본금_오류",
                        "업종": 업종,
                        "발견값": f"{금액}억원",
                        "정확값": f"{정답}억원",
                    }
                )

    # 기술자 충원기한 체크 (50일이 아닌 30일)
    if re.search(r"50\s*일\s*(이?내|까지|안에)", text):
        errors.append(
            {
                "type": "기한_오류",
                "항목": "기술자 충원기한",
                "발견값": "50일",
                "정확값": "30일",
            }
        )

    return errors
